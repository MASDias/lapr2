    private void applicationComboboxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_applicationComboboxActionPerformed
        Event e = (Event) eventCombobox.getSelectedItem();
        for (int i = 0; i < e.getEventEmployeeList().size(); i++) {
            modelEventEmployee.addElement(e.getEventEmployeeList().getEmployee(i));
        }

    }//GEN-LAST:event_applicationComboboxActionPerformed
    private void populateCombox() {
        for (int i = 0; i < eventRegistry.size(); i++) {
            eventCombobox.addItem(eventRegistry.getEvent(i));
        }
    }
    private void eventComboboxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eventComboboxActionPerformed
        applicationCombobox.removeAllItems();
        for (int i = 0; i < eventRegistry.getEvent(eventCombobox.getSelectedIndex()).getApplicationsList().size(); i++) {
            applicationCombobox.addItem(eventRegistry.getEvent(eventCombobox.getSelectedIndex()).getApplicationsList().getApplication(i));
        }
       modelEventEmployee.clear();
        for (int i = 0; i < eventRegistry.getEvent(eventCombobox.getSelectedIndex()).getEventEmployeeList().size(); i++) {
            modelEventEmployee.addElement(eventRegistry.getEvent(eventCombobox.getSelectedIndex()).getEventEmployeeList().getEmployee(i));
        }
    }//GEN-LAST:event_eventComboboxActionPerformed


